package techno.k9.onesignalv2point134.Beans;

public class BeanResult {

    private String url1;
    private String url2;
    private String url3;
    private String url4;
    private String url5;


    public BeanResult() {
    }

    public BeanResult(String url1, String url2, String url3, String url4, String url5) {
        this.url1 = url1;
        this.url2 = url2;
        this.url3 = url3;
        this.url4 = url4;
        this.url5 = url5;
    }


    public String getUrl1() {
        return url1;
    }

    public String getUrl2() {
        return url2;
    }

    public String getUrl3() {
        return url3;
    }

    public String getUrl4() {
        return url4;
    }

    public String getUrl5() {
        return url5;
    }
}


